def validate_spec(s):
    """Fail with a clear message if the spec is structurally invalid.

    Checks: required tables/keys present, nav ids unique, id_min_len <= id_max_len,
    threshold values are numeric. Raises SystemExit with a human-readable message
    instead of letting a KeyError/TypeError surface as a raw traceback.
    """
    errors = []

    # Required top-level tables and their required keys.
    required = {
        "identity": ["default_garden", "id_charset", "id_min_len", "id_max_len"],
        "token": ["secret_prefix", "secret_delimiter", "slot_current", "slot_previous"],
        "kv": ["garden_prefix", "dev_segment"],
        "headers": ["garden_id", "device_id", "node_id", "trace_id", "auth",
                    "capture_ts", "capture_batch", "trigger"],
        "thresholds": ["node_offline_after_secs", "rain_telemetry_fresh_secs",
                       "mitigate_threshold"],
        "archive": ["seconds_per_day"],
        "control": ["commands"],
        "ui": ["asset_version", "nav"],
    }
    for table, keys in required.items():
        if table not in s:
            errors.append(f"missing required table [{table}]")
            continue
        for key in keys:
            if key not in s[table]:
                errors.append(f"missing required key [{table}].{key}")

    if errors:
        raise SystemExit("gen: spec validation failed:\n" + "\n".join(f"  - {e}" for e in errors))

    g = s["identity"]
    th = s["thresholds"]

    # id length ordering
    lo, hi = g["id_min_len"], g["id_max_len"]
    if not isinstance(lo, int) or not isinstance(hi, int):
        errors.append("identity.id_min_len and id_max_len must be integers")
    elif lo > hi:
        errors.append(
            f"identity.id_min_len ({lo}) must be <= id_max_len ({hi})"
        )

    # threshold values must be numeric
    for name in ("node_offline_after_secs", "rain_telemetry_fresh_secs",
                 "mitigate_threshold"):
        v = th.get(name)
        if not isinstance(v, (int, float)):
            errors.append(f"thresholds.{name} must be numeric, got {type(v).__name__}")

    # nav ids must be unique
    nav = s["ui"].get("nav", [])
    seen_ids = set()
    for item in nav:
        nid = item.get("id")
        if nid is None:
            errors.append("a [[ui.nav]] entry is missing the 'id' key")
        elif nid in seen_ids:
            errors.append(f"duplicate nav id: {nid!r}")
        else:
            seen_ids.add(nid)

    if errors:
        raise SystemExit("gen: spec validation failed:\n" + "\n".join(f"  - {e}" for e in errors))


def main():
    spec = tomllib.loads(SPEC.read_text())
    validate_spec(spec)
    targets = {RS_OUT: render_rust(spec), PY_OUT: render_python(spec), FIX_OUT: render_fixture(spec)}
    check = "--check" in sys.argv[1:]
    drift = []
    for path, content in targets.items():
        current = path.read_text() if path.exists() else None
        if current != content:
            drift.append(path)
            if not check:
                path.write_text(content)
    rel = [str(p.relative_to(ROOT)) for p in drift]
    if check:
        if drift:
            print("gen-check: generated files are STALE — run `make gen`:")
            for r in rel:
                print(f"  - {r}")
            sys.exit(1)
        print("gen-check: generated files match contract/spec.toml ✓")
    else:
        print("gen: wrote " + (", ".join(rel) if rel else "nothing (already up to date)"))


if __name__ == "__main__":
    main()
