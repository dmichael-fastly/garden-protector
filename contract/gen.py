#!/usr/bin/env python3
"""contract/gen.py — render contract/spec.toml into the generated cross-language
contract modules (CHARTER: one identity/contract source of truth).

    python contract/gen.py            # write the generated files
    python contract/gen.py --check    # exit 1 if regenerating would change the tree

Outputs (each carries a GENERATED header):
    backend/src/contract_gen.rs    (Rust pub consts + pure fns + a cargo test)
    provision/contract_gen.py      (Python consts + pure fns)
    tests/contract_fixture.json    (expected outputs, for the conformance test)

The generated Rust is PURE (consts + String formatting only) — no host-ABI calls —
so it links under both wasm32 and native `cargo test`.
"""
import json
import pathlib
import sys
import tomllib

ROOT = pathlib.Path(__file__).resolve().parent.parent
SPEC = ROOT / "contract" / "spec.toml"
RS_OUT = ROOT / "backend" / "src" / "contract_gen.rs"
PY_OUT = ROOT / "provision" / "contract_gen.py"
FIX_OUT = ROOT / "tests" / "contract_fixture.json"

NOTE = "GENERATED by contract/gen.py from contract/spec.toml — DO NOT EDIT. Run `make gen`."


def fmt_f32_rust(v):
    """Render a float as a Rust f32 literal: deterministic decimal + explicit _f32 suffix.

    Uses repr() for exact round-trip fidelity, then appends the _f32 type suffix so the
    Rust compiler knows this is f32, not f64. This avoids str() surprises (e.g. scientific
    notation for small values like 1e-05) and prevents silent precision loss if the spec
    value ever changes.
    """
    s = repr(float(v))
    # repr() always includes a decimal point for finite floats, so no extra '.' needed.
    return f"{s}_f32"


def fmt_float_py(v):
    """Render a float as a Python literal: deterministic decimal, no scientific notation.

    Uses repr() for exact round-trip fidelity. repr() of common threshold values like
    0.3 yields '0.3', which is both human-readable and unambiguous as a Python literal.
    """
    return repr(float(v))


def charset_class(tokens):
    """A regex/char-class body from spec tokens, e.g. ['a-z','0-9','-'] -> 'a-z0-9-'."""
    return "".join(tokens)


def charset_rust(tokens):
    """A Rust byte predicate over `b` from spec tokens (ranges 'a-z' + literals)."""
    parts = []
    for t in tokens:
        if len(t) == 3 and t[1] == "-":
            parts.append(f"(b'{t[0]}'..=b'{t[2]}').contains(&b)")
        elif len(t) == 1:
            parts.append(f"b == b'{t}'")
        else:
            raise SystemExit(f"gen: unsupported id_charset token {t!r}")
    return " || ".join(parts)


def nav_items(s):
    """The [[ui.nav]] table as (id, href, label, viewer_ok, group) tuples (group "" if unset)."""
    return [(n["id"], n["href"], n["label"], bool(n["viewer_ok"]), n.get("group", ""))
            for n in s["ui"]["nav"]]


def render_nav_str(nav, active_id, view_only):
    """Reference render of the shared nav. The generated render_nav() in BOTH languages must
    produce this byte-for-byte (pinned by the fixture + cargo gen_tests). A contiguous run of
    items sharing a non-empty `group` is wrapped in a <details> dropdown (label = capitalized
    group); a run with no visible items (e.g. all admin links on the view-only edge) emits
    nothing. The <details> gets class "active" when the active page is inside it."""
    out = []
    n = len(nav)
    i = 0
    while i < n:
        nid, href, label, viewer_ok, group = nav[i]
        if not group:
            if not (view_only and not viewer_ok):
                full = "nav-" + nid
                cls = ' class="active"' if active_id == full else ""
                out.append(f'  <a href="{href}" id="{full}"{cls}>{label}</a>')
            i += 1
            continue
        run = []
        while i < n and nav[i][4] == group:
            it = nav[i]
            if not (view_only and not it[3]):
                run.append(it)
            i += 1
        if not run:
            continue
        gid = "nav-" + group
        gactive = any(active_id == "nav-" + it[0] for it in run)
        gcls = " active" if gactive else ""
        glabel = group[:1].upper() + group[1:]
        out.append(f'  <details class="nav-group{gcls}" id="{gid}"><summary>{glabel}</summary><div class="nav-group-menu">')
        for it in run:
            full = "nav-" + it[0]
            cls = ' class="active"' if active_id == full else ""
            out.append(f'    <a href="{it[1]}" id="{full}"{cls}>{it[2]}</a>')
        out.append("  </div></details>")
    return "\n".join(out)


def nav_literal(nav, true_lit, false_lit):
    """The NAV tuple-array body for a target language (its true/false literals differ)."""
    rows = [
        f'    ({json.dumps(nid)}, {json.dumps(href)}, {json.dumps(label)}, '
        f"{true_lit if viewer_ok else false_lit}, {json.dumps(group)})"
        for nid, href, label, viewer_ok, group in nav
    ]
    return "\n" + ",\n".join(rows) + ",\n"


